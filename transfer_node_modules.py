#!/usr/bin/env python3
"""Simple HTTP server to transfer node_modules.tar.gz to Raspberry Pi"""
import http.server
import socketserver
import os

PORT = 8888
DIRECTORY = os.path.dirname(os.path.abspath(__file__))

class MyHTTPRequestHandler(http.server.SimpleHTTPRequestHandler):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, directory=DIRECTORY, **kwargs)

os.chdir(DIRECTORY)
with socketserver.TCPServer(("", PORT), MyHTTPRequestHandler) as httpd:
    print(f"Serving at http://0.0.0.0:{PORT}")
    print(f"\nOn your Raspberry Pi, run:")
    print(f"cd /var/www/pisoprint")
    print(f"wget http://YOUR_WINDOWS_IP:{PORT}/node_modules.tar.gz")
    print(f"tar -xzf node_modules.tar.gz")
    print(f"rm node_modules.tar.gz")
    print(f"\nPress Ctrl+C to stop the server after download completes")
    httpd.serve_forever()
